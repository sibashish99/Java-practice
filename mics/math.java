public class math {
    public static void main(String arg[]){
        int x=15;
        int y=5;
        int maxi=Math.max(x,y);
        System.out.println("Max value is: "+maxi);
        int mini=Math.min(x,y);
        System.out.println("Min value is: "+mini);

        int s=5;
        double q = Math.sqrt(s);
        System.out.println("SQRT value is: "+q);

        double v=-45;
        double z=Math.abs(v);
        System.out.println("ABS value is: "+z);

        double p=Math.random();
        System.out.println("No is: "+p);


    }
}
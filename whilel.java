public class whilel {
    public static void main(String arg[]){
        //while loop
        int i = 0;
        while (i < 5) {
         System.out.println(i);
         i++;
         }
        
         //for
         String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
         for (String j : cars) {
              System.out.println(j);
            }
    }
}